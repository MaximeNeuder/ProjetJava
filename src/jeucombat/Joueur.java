    public void Attack(Joueur j)
    {
        int damage = 1;
        if (j.GetAction().equals("h")  || (j.GetAction().equals("c") && j.GetPosition().equals(this.position)) )
        {
            damage = 0;
        }
        if (j.GetAction().equals("t"))
        {
            damage = 2;
        }
        
        j.life = j.life - damage;
    }
    
    public void Throw(Joueur j)
    {
        int damage = 1;
        if (j.GetAction().equals("p"))
        {
            damage = 0;
        }
        if (j.GetAction().equals("c"))
        {
            damage = 2;
        }
        
        j.life = j.life - damage;
    }
    
    public void Counter(Joueur j)
    {
        int damage = 1;
        if(!j.GetAction().equals("p") && !j.GetPosition().equals(j.GetPosition()))
        {
            damage = 0;
        }
        else
        {
            damage = 2;
        }
        
        j.life = j.life - damage;
    }
    
